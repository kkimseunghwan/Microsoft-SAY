# HTTP통신
# 클라이언트가 서버에게 요청
# 서버는 그 요청에 대한 응답

# 요청에는 2가지 종류가 있음
#   GET 방식 : 주소를 직접 입력, <a>
#   POST 방식 : 특별한 상황

# 인터넷 주소 체계
