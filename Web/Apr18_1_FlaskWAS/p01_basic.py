# 내 컴퓨터에 접속하는 방법?
# 1) 내 아이피 주소를 통한 입력
# 2) 등록한 DNS 주소로 접속 > www.hwan.com
# 3) 내가 내컴에 접속하는 거라면, localhost 입력으로 접근 가능
# 4) (3)과 동일하게 127.0.0.1 로도 접속 가능

